//CREAMOS CADA FUNCION Y LA EXPORTAMOS
export function Suma(n1, n2){
    return n1 + n2;
}

export function Restar(n1, n2){
    return n1 - n2;
}

export function Multiplicar(n1, n2){
    return n1 * n2;
}

export function Division(n1, n2){
    return n1 / n2;
}